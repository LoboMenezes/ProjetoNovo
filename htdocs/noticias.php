<p>Os lobos são animais mamíferos da família Canidae, sendo o representante selvagem da família que também inclui a raposa e o cachorro.</p>
<p>Existem três espécies de lobos: o lobo-cinzento (Canis lupus), o lobo-etíope (Canis simensis) e o lobo-vermelho (Canis rufus), além de muitas subespécies. Só de Canis lupus existem 31 subespécies.</p>
